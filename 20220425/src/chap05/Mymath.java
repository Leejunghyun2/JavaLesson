package chap05;

public class Mymath {

	int add(int x, int y) {
		return x + y ;
		
	}
	int add(int x, int y,int z) {
		return x + y + z ;
		
	}
	int sub(int x, int y) {
		return x - y ;
		
	}
	int multi(int x, int y) {
		return x * y ;
		
	}
	double divide(double x, double y) {
		return x / y;
		
	}
	
}
