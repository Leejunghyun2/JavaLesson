package HashSet;

import java.util.ArrayList;
import java.util.HashSet;
import java.util.Set;

public class HashSetEx1 {
	public static void main(String[] args) {
		for (int j = 1; j < 6; j++) {
			for (int i = 6; i > j; i--) {
				System.out.print("*");
			}
			System.out.println();
		}
		for (int j = 1; j < 6; j++) {
			for (int i = 6; i > j; i--) {
				System.out.print("*");
			}
			System.out.println();
		}
	}
}
